from langgraph.graph import MessagesState
from typing import Annotated, Optional, List
from pathlib import Path

class CurrentState(MessagesState):
    """Tracks the state of the Football Analyst Agentic System across steps.
    Includes user query and intermediate file paths generated by the data analyst, 
    database manager, and visualization components.
    """
    data_analyst_file: Annotated[Optional[Path], "Path to the output CSV file from the data analyst server"]
    sqlite_database_manager: Annotated[Path, "Path to SQLite DB used for structured storage"]
    visualizer: Annotated[Path, "Path to the visualization output file (e.g., plot image)"]